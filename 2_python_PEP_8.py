"""
    Whitespace:
        Use spaces instead of tabs for indentation
        Use four spaces for each level of syntactically significant indenting
        Lines should be 79 characters in length or less
        Continuations of long expressions onto additional lines should be 
            indented by four extra spaces from their normal indentation level
        In a file, functions and classes should be separated by two blank line
        In a class, functions should be separated by one blank line
        No space around list indexes, function calls or keywprd argument
        Only one space before and after variable assignments
"""

"""
    Naming:
        Functions, variables and attributes should be in lowercase)underscore
            format
        Protected instance attributes: _leading_underscore format
        Private instance attributes: __double_leading_underscore format
        Class and exceptions should be in CapitalizedWord format
        Module-level constants should be in ALL_CAPS format
"""

"""
    Expressions and Statements:
        Use inline negation (if a is not b) instead of (if not a is b)
        Use (if not list) instead of (if len(list) == 0) to check empty list
        Avoid single line while and if statement
        Import session order:
            standard library
            third-party library
            own modules
"""
